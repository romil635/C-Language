#include<iostream>
#include<math.h>
#include<stdlib.h>
using namespace std;
int main()
{

    cout<<"ans is:"<<ceil(2.8)<<endl;
    cout<<"ans is:"<<ceil(-5.4)<<endl;

    cout<<"ans is:"<<floor(2.8)<<endl;
    cout<<"ans is:"<<floor(-5.4)<<endl;

    cout<<"ans is:"<<round(2.8)<<endl;
    cout<<"ans is:"<<round(-5.4)<<endl;

    cout<<"ans is:"<<abs(2.8)<<endl;
    cout<<"ans is:"<<abs(-5.4)<<endl;

    cout<<"ans is:"<<sqrt(16)<<endl;
    cout<<"ans is:"<<sqrt(5)<<endl;

    cout<<"ans is:"<<pow(2,5)<<endl;

    cout<<"ans is:"<<rand()%20<<endl;
    
}

